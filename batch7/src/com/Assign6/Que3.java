package com.Assign6;
//write a program  to print all even number from 50 to 75.
public class Que3 {

	public static void main(String[] args) {
		System.out.println("List of even numbers from 50 to 75 : ");
		
		for(int no=50;no<=75;no++) {
			if (no%2==0) {
				System.out.println(no);
			}
			
		}
		
	}

}
