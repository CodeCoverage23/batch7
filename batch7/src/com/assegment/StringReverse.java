package com.assegment;

public class StringReverse {

	 public static void main(String[] args) {
		
		 String name ="sister";
		 //789456123
		 String Rev ="";
		 for( int i=name.length () -1; i>=0; i--) {
			Rev=Rev+name.charAt(i);
			
		 }
			
		 System.out.println("the String Reverse: "+ Rev);
		 
		 
		 
		 
		 
	}}
	
	

