package com.pradeep.java8;

public class Students {

	int id;
	String name;
	int marks;
	public Students(int id, String name, int marks) {
		super();
		this.id = id;
		this.name = name;
		this.marks = marks;
	}
	@Override
	public String toString() {
		return "Students [id=" + id + ", name=" + name + ", marks=" + marks + "]";
	}
	
	
	
	
	
	
	
}
