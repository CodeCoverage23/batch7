package com.swatibasic;

public class Evennumberforloop {
	public static void main(String[] args) {

			int number=100;
			
			System.out.print("list of even number from 1 to 100"  );
			
			for(int i= 1;i<=number; i++)
			{
				if(i%2==0)
				{
				System.out.print(i+" ");
				
			}
					
				
						
			}

		}

	} 
