package com.Nilima.basic;

public class EvenNumber {

	public static void main(String[] args) {
		
		int number=100;
		System.out.println("the list of even number from 1 to 100");
		
		
		for (int i=1; i<=number; i++) {
			 
			if (i%2==0) {
				
				
				System.out.println(i+" ");
			}
		}
	}
}
