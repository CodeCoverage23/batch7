/*Assignment 6.1 Write a program to print 25 to 50 numbers
using while loop*/

package com.assignments;

public class Numbers {
	
	public static void main(String[] args) {
		
		int i = 25;
		
		System.out.println("Output is : ");
		
		while(i<=50)
		{
			System.out.println(i);
			i++;
			
		}
		
	}
	
	}

